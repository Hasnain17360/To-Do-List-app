let fruitChaat = ["Mango", "Banana", "Apple"];
console.log(fruitChaat);
// .push()
fruitChaat.push("Oranges"); // this will be added at the end
console.log(fruitChaat);
// .pop()
fruitChaat.pop(); // last element will be removed
console.log(fruitChaat);
// concat([]): this will be added as new array (elements) at the end
fruitChaat = fruitChaat.concat(['Melon', 'Grapes']);
console.log(fruitChaat);
